//Image Click
document.getElementById('image1').addEventListener('click', function() {
	if(document.getElementById("image1").style.width == "600px") {
	    document.getElementById("image1").style.width ="200px";
        document.getElementById("image1").style.height ="200px";


	}
 
    else{
	    document.getElementById("image1").style.width ="600px";
        document.getElementById("image1").style.height ="600px";
        document.getElementById("image1").style.filter ="grayscale(0%)";


   }
});
